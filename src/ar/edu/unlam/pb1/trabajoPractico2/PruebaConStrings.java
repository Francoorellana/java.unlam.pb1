package ar.edu.unlam.pb1.trabajoPractico2;

public class PruebaConStrings {
	public static void main(String arg[]) {
		
		String minuscula = "java es case sensitive";
		String mayuscula = minuscula.toUpperCase();
		
		System.out.println(minuscula.charAt(0));
		System.out.println(minuscula.length());
		System.out.println(minuscula.toLowerCase());
		System.out.println(minuscula.toUpperCase());
		System.out.println(mayuscula+".");
		
	}
	

}
